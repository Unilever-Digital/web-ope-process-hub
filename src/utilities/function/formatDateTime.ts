import dayjs from "dayjs";

export function formatDateTime(dateTimeString: string, format?: string) {
  const dateFormat = format ?? "HH:mm:ss DD/MM/YYYY";
  if (!dateTimeString) return "";
  return dayjs(dateTimeString).format(dateFormat);
}

export function formatDateTimeADX(date: Date): string {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  const hours = String(date.getHours()).padStart(2, "0");
  const minutes = String(date.getMinutes()).padStart(2, "0");
  const seconds = String(date.getSeconds()).padStart(2, "0");

  return `${year}-${month}-${day} ${hours}:${minutes}:${seconds}`;
}

